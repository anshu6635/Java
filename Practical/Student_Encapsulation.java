package CoreJava;

class Student_Encapsulation {
//Encapsulation is something that -->binding data with method's
	private int rollno;
	private String name;
	
	// Getter and Setter
	
	public int getRollno() {
		return rollno;
	}
	public void setRollno(int rollno) {
		this.rollno = rollno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
