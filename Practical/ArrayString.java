package CoreJava;
//Write a program to Declare and Traverse   string Array

public class ArrayString {

	public static void main(String[] args)
	{ 
		//Declaration and instantiation of string array
		
		String a[] = {"Anshu","Rahul","Priyana"};
		
		//Traversing 
		for (int i = 0 ; i<a.length; i++)
		{
			System.out.println(a[i]);
		}
		
		

	}

}
